/*
Ejercicio 6: Buscar la última posición de un elemento repetido
Tienes una lista de números [2, 5, 7, 2, 8, 7]. Encuentra y muestra 
la última posición en la que aparece el número 7.
 */

let listaNum = [2, 5, 7, 2, 8, 7];
let posicion = listaNum.lastIndexOf(7);

console.log("La última posición del número 7 es: ",posicion);
